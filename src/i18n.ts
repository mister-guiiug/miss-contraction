export function isSupportedLanguage(value: unknown): value is AppLanguage {
  return typeof value === 'string' && LANGUAGE_SET.has(value);
}

export function detectBrowserLanguage(): AppLanguage {
  if (typeof navigator === 'undefined') return 'fr';
  const primary = navigator.language?.slice(0, 2).toLowerCase() ?? 'fr';
  return isSupportedLanguage(primary) ? primary : 'fr';
}

export function t(language: AppLanguage, key: string): string {
  return MESSAGES[language][key] ?? MESSAGES.en[key] ?? MESSAGES.fr[key] ?? key;
}

export function interpolate(
  input: string,
  values: InterpolationValues
): string {
  return input.replace(/\{(\w+)\}/g, (_, name: string) =>
    String(values[name] ?? `{${name}}`)
  );
}
